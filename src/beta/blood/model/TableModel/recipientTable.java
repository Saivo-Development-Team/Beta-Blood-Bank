package beta.blood.model.TableModel;

/**
 *
 * @author Admin
 */
public class recipientTable {
    String recid, name, address, tel, email;

    public recipientTable(String recid, String name, String address, String tel, String email) {
        this.recid = recid;
        this.name = name;
        this.address = address;
        this.tel = tel;
        this.email = email;
    }

    public String getRecid() {
        return recid;
    }

    public void setRecid(String recid) {
        this.recid = recid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
}
